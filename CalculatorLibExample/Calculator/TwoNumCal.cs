using System;

namespace Calculator
{
    public static class TwoNumCal
    {
        public static int TwoNumSum(int numOne , int numTwo)
        {
            return numOne + numTwo;
        }
        public static int TwoNumProduct(int numOne, int numTwo)
        {
            return numOne * numTwo;
        }
        public static int TwoNumSubtraction(int numOne, int numTwo)
        {
            return numOne - numTwo;
        }
        public static int TwoNumDivision(int numOne, int numTwo)
        {
            return numOne / numTwo;
        }
        public static int NumMod(int numOne, int numTwo)
        {
            return numOne % numTwo;
        }

    }
}
